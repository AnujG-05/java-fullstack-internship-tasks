public class IncDec {
    public static void main(String[] args) {
       int a=10;
       System.out.println(a++); //10 (prints then increments)
       System.out.println(a);
       System.out.println(++a); //12 (increments then prints)
       System.out.println(a);
        int b= 12;
         System.out.println(b--); //12 (prints then decrements)
            System.out.println(b);
            System.out.println(--b); //10 (decrements then prints)
            System.out.println(b);
    }
}
